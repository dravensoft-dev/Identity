/* Compiles Arena's Tailwind layer to a file a browser can load.
 *
 * The gates compile the layer into a temp dir and throw it away — that is right
 * for a gate and useless for a specimen page, which is static HTML and cannot
 * run a compiler. So the compiled utilities are build output in the tree, on
 * exactly the terms tokens/*.css already are: authored source (theme.css + the
 * manifests), a build script, committed output, and check-tailwind-generated.mjs
 * failing the day the two disagree.
 *
 * The content set is the manifests and nothing else, inherited from
 * compileLayer() — so a class that no manifest declares does not exist here,
 * and a specimen page cannot invent styling the manifest does not carry.
 *
 * Each manifest also gets a `.manifest.ts` sibling — `export default {…} as
 * const;`. A plain `import … from '*.manifest.json'` resolves under
 * `resolveJsonModule`, but TypeScript widens every JSON property to its base
 * type (`string`, not the literal union a recipe's `defaultVariants` needs —
 * verified against the real compiler in frameworks/angular/tsconfig.check.json,
 * where `tv(manifest)` only typechecks against the `as const` module). The
 * JSON stays the source of truth and the authored artifact; the `.ts` is build
 * output like utilities.css, and an Angular recipe imports it instead of the
 * JSON directly.
 *
 *   bun scripts/build-tailwind.mjs      -> writes frameworks/tailwind/utilities.css
 *                                          and a frameworks/tailwind/components/*.manifest.ts
 *                                          beside every *.manifest.json
 */
import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { join } from 'node:path';
import { compileLayer, repoRoot } from './lib/tailwind-compile.mjs';

export const BANNER = '/* GENERATED by scripts/build-tailwind.mjs — edit frameworks/tailwind/theme.css or a manifest, not this file. */\n';

/** The banner for a generated `.manifest.ts` module, naming its actual source
 *  — the sibling `.manifest.json` — rather than BANNER's generic "a manifest",
 *  which on a file already named `X.manifest.ts` points the reader at itself.
 *  @param {string} jsonFile e.g. "Tag.manifest.json" @returns {string} */
export function manifestBanner(jsonFile) {
  return `/* GENERATED by scripts/build-tailwind.mjs — edit ${jsonFile}, not this file. */\n`;
}

/** @param {{root?: string}} [opts] @returns {string} absolute path of the output */
export function generatedPath(opts = {}) {
  return join(opts.root ?? repoRoot, 'frameworks', 'tailwind', 'utilities.css');
}

/** Compile the layer and return the text that belongs in utilities.css.
 *  @param {{root?: string}} [opts] @returns {string} */
export function buildTailwind(opts = {}) {
  const { css } = compileLayer(opts);
  return BANNER + css;
}

/** Render a manifest as a `.manifest.ts` module carrying the same data
 *  `as const`, so a TypeScript consumer gets the manifest's literal string
 *  unions instead of the widened `string` a JSON import gives every property.
 *  @param {object} manifest @param {string} jsonFile e.g. "Tag.manifest.json"
 *  @returns {string} */
export function manifestModule(manifest, jsonFile) {
  return `${manifestBanner(jsonFile)}export default ${JSON.stringify(manifest, null, 2)} as const;\n`;
}

/** Every `.manifest.ts` sibling the current manifests compile to.
 *  @param {{root?: string}} [opts]
 *  @returns {Map<string, string>} absolute `.manifest.ts` path -> file contents */
export function buildManifestModules(opts = {}) {
  const { manifests } = compileLayer(opts);
  const components = join(opts.root ?? repoRoot, 'frameworks', 'tailwind', 'components');
  const out = new Map();
  for (const [file, manifest] of manifests)
    out.set(join(components, file.replace(/\.json$/, '.ts')), manifestModule(manifest, file));
  return out;
}

function main() {
  const text = buildTailwind();
  const path = generatedPath();
  writeFileSync(path, text);
  console.log(`build-tailwind: wrote ${path} (${text.length} bytes)`);

  for (const [tsPath, content] of buildManifestModules()) {
    writeFileSync(tsPath, content);
    console.log(`build-tailwind: wrote ${tsPath}`);
  }
}

if (process.argv[1] === fileURLToPath(import.meta.url)) main();
